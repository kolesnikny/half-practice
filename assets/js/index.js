function getSum(array, numberElements) {
    if (numberElements <= 0 || numberElements > array.length) {
        throw new RangeError('bed number');
    }
    let sum = 0;
    for (let i = 0; i < numberElements; i++) {
        sum += array[i];
    }
    return sum;
}

